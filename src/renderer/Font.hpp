// A UV rectangle into the atlas: (u0,v0) top-left .. (u1,v1) bottom-right, in
// normalized [0,1] texture coordinates. Two triangles per glyph read these.
struct UvRect {
    float u0, v0, u1, v1;
};

// Map a character to its cell index (row-major, 0..95). Anything outside the
// printable range — control chars, high bytes — falls back to '?' so unknown
// input renders a visible placeholder instead of garbage. (Newline/tab are
// handled by the Hud collector, which never calls this for them.)
[[nodiscard]] constexpr int glyphCell(char c) {
    const unsigned uc = static_cast<unsigned char>(c);
    if (uc < static_cast<unsigned>(kFirstGlyph) || uc > static_cast<unsigned>(kLastGlyph)) {
        return '?' - kFirstGlyph;
    }
    return static_cast<int>(uc) - kFirstGlyph;
}

// The atlas UV rect for a cell index. We inset by HALF A TEXEL on every side: the
// atlas packs cells edge-to-edge with no gutter, so a quad sampled with NEAREST
// filtering right at a cell boundary could `floor()` into the NEIGHBOURING cell
// and bleed a stray pixel (especially descenders like g/j/p spilling into the
// glyph below). Nudging the UVs inward keeps every sample strictly inside the
// intended 8x8 cell while still covering all eight texels.
[[nodiscard]] constexpr UvRect cellUV(int cell) {
    const int col = cell % kFontCols;
    const int row = cell / kFontCols;
    constexpr float halfU = 0.5f / static_cast<float>(kAtlasW);
    constexpr float halfV = 0.5f / static_cast<float>(kAtlasH);
    const float u0 = static_cast<float>(col * kGlyphPx) / static_cast<float>(kAtlasW);
    const float v0 = static_cast<float>(row * kGlyphPx) / static_cast<float>(kAtlasH);
    const float u1 = static_cast<float>((col + 1) * kGlyphPx) / static_cast<float>(kAtlasW);
    const float v1 = static_cast<float>((row + 1) * kGlyphPx) / static_cast<float>(kAtlasH);
    return {u0 + halfU, v0 + halfV, u1 - halfU, v1 - halfV};
}

