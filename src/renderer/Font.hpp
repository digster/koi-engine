// ============================================================================
//  Font.hpp — an embedded 8x8 bitmap font and its atlas layout
// ----------------------------------------------------------------------------
//  To draw TEXT we need glyph SHAPES, and the simplest way to store a shape is a
//  tiny monochrome bitmap: an 8x8 grid of on/off pixels. Each glyph is exactly
//  8 bytes — one byte per row, one bit per column (bit 0 = leftmost pixel). A '1'
//  bit is ink, a '0' bit is background. That's a "bitmap font": no curves, no
//  hinting, no external file — just 8 numbers per character that the renderer
//  bakes into a texture once.
//
//  This is the classic, public-domain `font8x8_basic` table (the IBM-PC-style
//  8x8 font). We embed it in code on purpose: the engine stays fully offline and
//  dependency-free (no .ttf, no font library), which matches Koi's from-scratch
//  ethos and lets you SEE exactly what a glyph is at the bit level.
//
//  THE ATLAS. A GPU can't sample "byte array"; it samples a TEXTURE. So at start-
//  up GpuRenderer walks this table and paints every glyph into one image — the
//  font ATLAS — laid out on a fixed grid of 8x8 cells. Drawing a character then
//  means drawing a quad whose UVs point at that character's cell. Two parties
//  must agree on the exact grid: the renderer that PAINTS the atlas and the Hud
//  collector that computes a glyph's UVs. This header is that single source of
//  truth — the layout constants and `cellUV()` live here so they can never drift.
//
//  This file is PURE DATA + constexpr math (no SDL, no GPU), so the Hud collector
//  that includes it stays fully unit-testable. Concepts are explained in
//  documentation/docs/24-hud-and-text.html.
// ============================================================================
#pragma once

